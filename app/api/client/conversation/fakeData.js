const getUserData = () => {
  const messageList = fakeData.filter(item => item.Messages.length > 0).map(item => item.Messages)
  const userOfAllMessages = messageList.map(message => {
    return message.map(item => item.User)
  }).reduce((pre, cur) => pre.concat(cur, []))
  const userIdDistinct = [...new Set(userOfAllMessages.map(item => item.id))]
  const userDistinct = userIdDistinct.map(id => {
    return userOfAllMessages.filter(item => item.id === id)[0]
  })
  const listData = userDistinct.map(user => {
    return `${user.id},${user.username},${user.role},${user.password},${user.color}`
  })
  return listData.join("\n")
}

const getMessageData = () => {
  const messageList = fakeData
    .filter(item => item.Messages.length > 0).map(item => item.Messages)
    .reduce((pre, cur) => pre.concat(cur, []))
  const listData = messageList.map(msg => {
    const contentAfterFormat = msg.content.replace(',', ' ')
    return `${msg.id},${msg.idConversation},${msg.idSender},${contentAfterFormat},${msg.type},${msg.sendAt}`
  })
  return listData.join("\n")
}
